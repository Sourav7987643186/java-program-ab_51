package ab51;

// MATH class

public class import_class 
{

	public static void main(String[] args)
	{
		System.out.println(Math.addExact(50, 150));      
		System.out.println(Math.addExact(40, 20));       
		System.out.println(Math.subtractExact(50, 150));
		System.out.println(Math.subtractExact(40, 20));
		System.out.println(Math.multiplyExact(20, 20));
		System.out.println(Math.multiplyExact(200000000000l, 10));
		System.out.println(Math.divideExact(200, 10));
		System.out.println(Math.divideExact(2000000000000l, 500000000));
		
		
		System.out.println(Math.max(450, 4));
		System.out.println(Math.max(2, 4));
		System.out.println(Math.max(1, 1));
		System.out.println(Math.max(01, 02));
		
		
		// absolute number always give output in positive number
		
		System.out.println(Math.abs(-50));
		
		// random - it gives random data types with double data value.
		// it give unique value
		
		System.out.println(Math.random());
		
		
		
		
	}

}
